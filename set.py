# A Set is a collection which is unordered and unindexed. No duplicate members.

# Create set
fruits_set = {'Apples', 'Oranges', 'Mango'}

# Check if in set
print ('Apples' in fruits_set)

# Add to set
fruits_set.add ('Grape')

# Remove from set
fruits_set.remove ('Grape')


# Add dublicate
fruits_set.add ('Apples')

# Clear set
fruits_set.clear ()

# Delete
del fruits_set

print (fruits_set)