# A Tuple is a collect which is ordered and unchangeable. Allows duplicate members.

# Create tuple
fruits = ('Apples', 'Oranges', 'Grapes')

# Using a constructor
# fruits2 = tuple (('Apples', 'Orange', Grapes))

# Single value needs trailing comma
fruits2 = ('Apples',)

# Get value
print (fruits [1])

# Can't chnage value
fruits [0] = 'Pears'

# Delete tuple
del fruits2

# Get length
print (len (fruits))
